  private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
    if(txtNama.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'Nama Tamu'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else if(txtAlamat.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'Alamat'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else if(txtTelepon.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'No. Telepon'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else if(txtTujuan.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'Tujuan'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else if(txtTglMasuk.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'Tanggal Masuk'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else if(txtWktMasuk.getText().trim().isEmpty()){
      JOptionPane.showMessageDialog(null, "Kolom 'Waktu Masuk'\nTidak Boleh Kosong","Perhatian",JOptionPane.WARNING_MESSAGE);
    } else{
      control.insert(this);
      config();
      reset();
    }
  }//GEN-LAST:event_btnProsesActionPerformed

