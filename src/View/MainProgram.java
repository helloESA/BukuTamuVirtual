  private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    if (tableTamu.getSelectedRow()>=0){
            int dialogResult = JOptionPane.showConfirmDialog(this, "Yakin Ingin Menghapus Data Ini?","Peringatan",JOptionPane.WARNING_MESSAGE);
            if(dialogResult == JOptionPane.YES_OPTION){
                String id = tableTamu.getValueAt(tableTamu.getSelectedRow(),0).toString();
                control.delete(id);
                config();
                reset();
            }
        } else{
            JOptionPane.showMessageDialog(this, "Pilih Data Pada Tabel Sebelum Hapus","Kesalahan",JOptionPane.WARNING_MESSAGE);
        }
  }//GEN-LAST:event_btnHapusActionPerformed
    
