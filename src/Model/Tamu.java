package Model;

/**
 *
 * @author MohamadEsa
 */
public class Tamu {
  String  id, 
          nama_tamu, 
          alamat, 
          no_telepon, 
          tujuan_tamu, 
          tanggal_masuk, 
          waktu_masuk, 
          waktu_keluar;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getNama_tamu() {
    return nama_tamu;
  }

  public void setNama_tamu(String nama_tamu) {
    this.nama_tamu = nama_tamu;
  }

  public String getAlamat() {
    return alamat;
  }

  public void setAlamat(String alamat) {
    this.alamat = alamat;
  }

  public String getNo_telepon() {
    return no_telepon;
  }

  public void setNo_telepon(String no_telepon) {
    this.no_telepon = no_telepon;
  }

  public String getTujuan_tamu() {
    return tujuan_tamu;
  }

  public void setTujuan_tamu(String tujuan_tamu) {
    this.tujuan_tamu = tujuan_tamu;
  }

  public String getTanggal_masuk() {
    return tanggal_masuk;
  }

  public void setTanggal_masuk(String tanggal_masuk) {
    this.tanggal_masuk = tanggal_masuk;
  }

  public String getWaktu_masuk() {
    return waktu_masuk;
  }

  public void setWaktu_masuk(String waktu_masuk) {
    this.waktu_masuk = waktu_masuk;
  }

  public String getWaktu_keluar() {
    return waktu_keluar;
  }

  public void setWaktu_keluar(String waktu_keluar) {
    this.waktu_keluar = waktu_keluar;
  }
  
  
}
